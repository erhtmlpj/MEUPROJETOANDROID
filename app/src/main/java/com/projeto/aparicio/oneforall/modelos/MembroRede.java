public class MembroRede {
    String codigoMembroRede;
    String IPv4;
    String nome;
    String codigoRede;

    public String getCodigoMembroRede() {
        return codigoMembroRede;
    }

    public void setCodigoMembroRede(String codigoMembroRede) {
        this.codigoMembroRede = codigoMembroRede;
    }

    public String getIPv4() {
        return IPv4;
    }

    public void setIPv4(String IPv4) {
        this.IPv4 = IPv4;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getCodigoRede() {
        return codigoRede;
    }

    public void setCodigoRede(String codigoRede) {
        this.codigoRede = codigoRede;
    }
}
