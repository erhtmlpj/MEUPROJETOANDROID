public class Membro {
    String codigoMembro;
    String codigoRede;
    String codigoUsuario;

    public String getCodigoMembro() {
        return codigoMembro;
    }

    public void setCodigoMembro(String codigoMembro) {
        this.codigoMembro = codigoMembro;
    }

    public String getCodigoRede() {
        return codigoRede;
    }

    public void setCodigoRede(String codigoRede) {
        this.codigoRede = codigoRede;
    }

    public String getCodigoUsuario() {
        return codigoUsuario;
    }

    public void setCodigoUsuario(String codigoUsuario) {
        this.codigoUsuario = codigoUsuario;
    }
}
